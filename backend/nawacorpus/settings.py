def database_config() -> dict[str, object]:
    if os.getenv("DATABASE_URL"):
        raise RuntimeError(
            "DATABASE_URL parsing is intentionally not bundled. Use POSTGRES_* "
            "environment variables so local setup stays dependency-light."
        )

    if os.getenv("USE_SQLITE", "").lower() in {"1", "true", "yes"}:
        return {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }

    return {
        "ENGINE": "django.db.backends.postgresql",
        "NAME": os.getenv("POSTGRES_DB", "nawat_concordance"),
        "USER": os.getenv("POSTGRES_USER", "nawat"),
        "PASSWORD": os.getenv("POSTGRES_PASSWORD", "nawat"),
        "HOST": os.getenv("POSTGRES_HOST", "localhost"),
        "PORT": os.getenv("POSTGRES_PORT", "5432"),
    }


